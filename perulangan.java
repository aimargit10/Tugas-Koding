public class perulangan {

    public static void main(String[] args) {
        // Inisialisasi variabel
        int i = 1;

        // Perulangan
        while (i <= 117) {
            // Cetak bilangan
            System.out.println(i);

            // Increment variabel
            i++;
            if (i>=9){
                System.out.println("ucok");
            }
        }
    }

}
